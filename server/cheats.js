// ║  ЧИТЫ (тестовый режим). СЕКРЕТ: этот модуль живёт ТОЛЬКО на сервере —        ║
// ║  коды читов НЕ попадают в клиент (ни в исходники, ни в devtools). Клиент     ║
// ║  лишь шлёт введённую строку, сервер сверяет её с этим списком и применяет.   ║
// ║                                                                              ║
// ║  Список читов (для себя):                                                    ║
// ║    motherlode — +1000 золота                                                 ║
// ║    geraldford — спавнит чит-авианосец (5 снарядов залпом, очень живуч)        ║
// ║    nofog      — переключить туман войны (клиентский визуал)                   ║
// ╚══════════════════════════════════════════════════════════════════════════╝
import { CHEATS_ENABLED } from './config.js';
import { spawnCheatShip } from './game.js';

// code -> функция(game, pIdx) -> { ok, msg?, broadcast?, effect? }
//   broadcast: true  — чит изменил состояние игры, нужно разослать стейт/сохранить
//   effect: 'токен'  — клиентский эффект (напр. туман); сервер состояние не трогает
const CHEATS = {
  motherlode(game, pIdx) {
    game.players[pIdx].gold += 1000;
    return { ok: true, msg: '💰 +1000 золота', broadcast: true };
  },
  geraldford(game, pIdx) {
    // не больше одного авианосца на игрока — иначе спамом кода можно наплодить флот
    if (game.ships.some(s => s.owner === pIdx && s.type === 'carrier'))
      return { ok: false, msg: 'У тебя уже есть авианосец' };
    const ship = spawnCheatShip(game, pIdx, 'carrier');
    return ship
      ? { ok: true, msg: '🛩 Авианосец спущен на воду', broadcast: true }
      : { ok: false, msg: 'Нет места у базы для авианосца' };
  },
  nofog() {
    // туман — чисто клиентский визуал; сервер лишь даёт клиенту команду переключить показ
    return { ok: true, msg: '🌫 Туман войны переключён', effect: 'toggleFog' };
  }
};

// Попытаться применить чит. Возвращает РЕЗУЛЬТАТ {ok,...} если строка — это код из списка,
// иначе null (тогда вызывающий обходится с текстом как с обычным сообщением — чат).
// Так коды не палятся: распознанный код НИКОГДА не уходит в чат, даже при выключенных читах.
export function applyCheat(game, pIdx, code) {
  const fn = CHEATS[String(code || '').trim().toLowerCase()];
  if (!fn) return null;                                   // не из списка → это просто текст (чат)
  if (!CHEATS_ENABLED) return { ok: false, msg: 'Сейчас недоступно' }; // код распознан, но режим выключен
  try {
    return fn(game, pIdx);
  } catch (e) {
    return { ok: false, msg: 'Ошибка: ' + e.message };
  }
}
